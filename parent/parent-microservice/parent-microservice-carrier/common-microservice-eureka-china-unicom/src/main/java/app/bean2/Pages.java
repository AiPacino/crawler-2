package app.bean2;

public class Pages {
	private int pageNo;

	private boolean curr;

	public void setPageNo(int pageNo) {
		this.pageNo = pageNo;
	}

	public int getPageNo() {
		return this.pageNo;
	}

	public void setCurr(boolean curr) {
		this.curr = curr;
	}

	public boolean getCurr() {
		return this.curr;
	}

}
